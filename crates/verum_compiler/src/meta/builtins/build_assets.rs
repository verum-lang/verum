//! Build Assets Intrinsics (Tier 1 - Requires BuildAssets)
//!
//! Provides compile-time file system access with security restrictions.
//! All functions in this module require the `BuildAssets` context since they
//! access the file system.
//!
//! ## File Loading
//!
//! | Function | Signature | Description |
//! |----------|-----------|-------------|
//! | `load_text(path)` | `(Text) -> Text` | Load file as text |
//! | `include_bytes(path)` | `(Text) -> Bytes` | Load file as bytes |
//! | `include_str(path)` | `(Text) -> Text` | Alias for load_text |
//!
//! ## File System Operations
//!
//! | Function | Signature | Description |
//! |----------|-----------|-------------|
//! | `asset_exists(path)` | `(Text) -> Bool` | Check if file exists |
//! | `asset_list_dir(path)` | `(Text) -> List<Text>` | List directory contents |
//! | `asset_metadata(path)` | `(Text) -> AssetMetadata` | Get file metadata |
//!
//! ## Security
//!
//! All paths are restricted to the project root and configured asset directories.
//! Path traversal (e.g., `..`) and absolute paths are not allowed.
//!
//! ## Context Requirements
//!
//! **Tier 1**: All functions require `using [BuildAssets]` context.
//!
//! Verum unified meta-system: all compile-time computation uses `meta` (meta fn,
//! @tagged_literal, @derive, @interpolation_handler). Multi-pass architecture:
//! Pass 1 parses and registers meta handlers, Pass 2 expands using complete
//! registry, Pass 3+ performs semantic analysis. Sandboxed execution (no I/O).
//! Meta context unification: all compile-time features desugar to meta-system
//! operations, providing one coherent model with convenient syntax sugar.

use verum_common::{List, Text};

use super::context_requirements::{BuiltinInfo, BuiltinRegistry};
use super::{ConstValue, MetaContext, MetaError};

/// Register build assets builtins with context requirements
///
/// All file system functions require BuildAssets context since they
/// access files from the project directory.
pub fn register_builtins(map: &mut BuiltinRegistry) {
    // ========================================================================
    // File Loading (Tier 1 - BuildAssets)
    // ========================================================================

    map.insert(
        Text::from("load_text"),
        BuiltinInfo::build_assets(
            meta_load_text,
            "Load file content as text",
            "(Text) -> Text",
        ),
    );
    map.insert(
        Text::from("include_str"),
        BuiltinInfo::build_assets(
            meta_load_text,
            "Load file content as text (alias for load_text)",
            "(Text) -> Text",
        ),
    );
    map.insert(
        Text::from("include_bytes"),
        BuiltinInfo::build_assets(
            meta_include_bytes,
            "Load file content as bytes",
            "(Text) -> Bytes",
        ),
    );
    // P7 (#20) — `@embed(path)` is the attribute-form alias for
    // `include_bytes(path)`. Both surface the same `meta_include_bytes`
    // dispatcher; the only difference is the @ macro-call syntax
    // accepted at expression positions outside a `meta fn`. Registered
    // under the bare name `embed` so the macro expander resolves
    // `@embed("…")` through the standard builtin lookup path.
    map.insert(
        Text::from("embed"),
        BuiltinInfo::build_assets(
            meta_include_bytes,
            "Embed a file's bytes at compile time (alias for include_bytes; \
             usable as an `@embed(\"path\")` macro call)",
            "(Text) -> Bytes",
        ),
    );

    // P7 (#20) — `@embed_glob(pattern)` returns the matched files
    // as `(Text, Bytes)` pairs (relative path, file bytes).
    // Single-level glob today (`*` / `?` in the basename
    // component); recursive `**` is reserved for follow-up.
    map.insert(
        Text::from("embed_glob"),
        BuiltinInfo::build_assets(
            meta_embed_glob,
            "Embed every file matching a glob pattern at compile time. \
             Supports `*`/`?` within a single path component and `**` \
             for recursive descent (must be a standalone path component, \
             e.g. `assets/**/*.png`). Returns List<(Text, Bytes)> \
             sorted lexicographically by path so generated bytecode is \
             deterministic across platforms. Walk depth capped at 64 \
             to prevent symlink-cycle stack exhaustion.",
            "(Text) -> List<(Text, Bytes)>",
        ),
    );

    // P7 (#20) — `@codegen("path/to/spec.toml")` MVP slice. Parses
    // a TOML document at compile time and returns it as a
    // `ConstValue::Map` keyed by Text. Use cases: declarative
    // schema → record-type derivations, build-time config tables,
    // structured asset manifests. The full attribute-form
    // `@codegen` (user meta-fn invocation over the parsed spec) is
    // a follow-up; this primitive is the foundation. Registered
    // under both `load_toml` (function-call form) and `codegen`
    // (macro-call attribute form) so either spelling resolves to
    // the same dispatcher.
    map.insert(
        Text::from("load_toml"),
        BuiltinInfo::build_assets(
            meta_load_toml,
            "Parse a TOML file at compile time. Returns a Map<Text, ConstValue>; \
             nested tables become nested Maps, arrays become Arrays, datetimes \
             collapse to RFC 3339 text. Sandbox-restricted to project root.",
            "(Text) -> Map<Text, Any>",
        ),
    );
    map.insert(
        Text::from("codegen"),
        BuiltinInfo::build_assets(
            meta_codegen,
            "@codegen(\"spec.toml\") — load a TOML codegen spec at compile \
             time, returning Map<Text, Any>.  Two-arg form \
             `codegen(\"spec.toml\", \"build_fn\")` additionally invokes \
             the user-defined meta fn `build_fn` with the parsed map and \
             returns whatever it produces (typically a List<Item> spliced \
             into the surrounding module).  Sandbox-restricted to project \
             root.",
            "(Text [, Text]) -> Any",
        ),
    );

    // ========================================================================
    // File System Operations (Tier 1 - BuildAssets)
    // ========================================================================

    map.insert(
        Text::from("asset_exists"),
        BuiltinInfo::build_assets(
            meta_asset_exists,
            "Check if asset file exists",
            "(Text) -> Bool",
        ),
    );
    map.insert(
        Text::from("asset_list_dir"),
        BuiltinInfo::build_assets(
            meta_asset_list_dir,
            "List directory contents",
            "(Text) -> List<Text>",
        ),
    );
    map.insert(
        Text::from("asset_metadata"),
        BuiltinInfo::build_assets(
            meta_asset_metadata,
            "Get file metadata (size, modified, type)",
            "(Text) -> (UInt, UInt, Bool, Bool, Bool)",
        ),
    );
}

