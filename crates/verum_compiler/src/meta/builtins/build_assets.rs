    #[test]
    fn test_embed_glob_recursive_pattern_walks_subtree() {
        let (mut ctx, temp_dir) = create_test_context();
        // assets/icons/error.png
        // assets/icons/info.png
        // assets/themes/dark/wallpaper.png
        // assets/themes/light/wallpaper.png
        // assets/notes.txt   (must NOT match *.png)
        let icons = temp_dir.path().join("assets").join("icons");
        let dark = temp_dir.path().join("assets").join("themes").join("dark");
        let light = temp_dir.path().join("assets").join("themes").join("light");
        std::fs::create_dir_all(&icons).unwrap();
        std::fs::create_dir_all(&dark).unwrap();
        std::fs::create_dir_all(&light).unwrap();
        std::fs::write(icons.join("error.png"), &[1u8]).unwrap();
        std::fs::write(icons.join("info.png"), &[2u8]).unwrap();
        std::fs::write(dark.join("wallpaper.png"), &[3u8]).unwrap();
        std::fs::write(light.join("wallpaper.png"), &[4u8]).unwrap();
        std::fs::write(temp_dir.path().join("assets").join("notes.txt"), b"prose").unwrap();

        let result = meta_embed_glob(
            &mut ctx,
            List::from(vec![ConstValue::Text(Text::from("assets/**/*.png"))]),
        )
        .expect("recursive embed_glob must succeed");

        match result {
            ConstValue::Array(pairs) => {
                assert_eq!(
                    pairs.len(),
                    4,
                    "expected 4 PNG matches across the tree, got {}",
                    pairs.len()
                );
                // Lexicographic ordering — matches must be
                // deterministic across platforms.
                let paths: Vec<&str> = pairs
                    .iter()
                    .map(|p| match p {
                        ConstValue::Tuple(t) => match &t[0] {
                            ConstValue::Text(s) => s.as_str(),
                            _ => panic!("expected Text"),
                        },
                        _ => panic!("expected Tuple"),
                    })
                    .collect();
                assert_eq!(paths[0], "assets/icons/error.png");
                assert_eq!(paths[1], "assets/icons/info.png");
                assert_eq!(paths[2], "assets/themes/dark/wallpaper.png");
                assert_eq!(paths[3], "assets/themes/light/wallpaper.png");
            }
            other => panic!("expected Array, got {:?}", other),
        }
    }

