#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn extract_type_name_handles_primitives() {
        let int = Type::new(TypeKind::Int, Span::default());
        assert_eq!(extract_type_name(&int).as_str(), "Int");

        let bool_ty = Type::new(TypeKind::Bool, Span::default());
        assert_eq!(extract_type_name(&bool_ty).as_str(), "Bool");
    }

    #[test]
    fn impl_bindings_empty_by_default() {
        let for_type = Type::new(TypeKind::Int, Span::default());
        let bindings = ImplBindings::from_impl_items(&for_type, &[]);
        assert!(bindings.types.is_empty());
        assert!(bindings.methods.is_empty());
        assert!(bindings.consts.is_empty());
    }

    #[test]
    fn lookup_value_returns_none_for_unknown() {
        let bindings = ImplBindings::default();
        assert!(bindings.lookup_value("nonexistent").is_none());
    }

    #[test]
    fn substitute_self_path_alone_becomes_for_type() {
        let for_type = Type::new(TypeKind::Int, Span::default());
        let bindings = ImplBindings::default();
        let path = Path::single(Ident::new(Text::from("Self"), Span::default()));
        let result = substitute_self_in_path(&path, &for_type, &bindings);
        assert_eq!(result.segments.len(), 1);
        match &result.segments[0] {
            PathSegment::Name(ident) => assert_eq!(ident.name.as_str(), "Int"),
            _ => panic!("expected Name segment"),
        }
    }

    #[test]
    fn substitute_self_dot_member_no_binding_uses_for_type_head() {
        let for_type = Type::new(TypeKind::Int, Span::default());
        let bindings = ImplBindings::default();
        let path = Path {
            segments: smallvec![
                PathSegment::Name(Ident::new(Text::from("Self"), Span::default())),
                PathSegment::Name(Ident::new(Text::from("Elem"), Span::default())),
            ],
            span: Span::default(),
        };
        let result = substitute_self_in_path(&path, &for_type, &bindings);
        assert_eq!(result.segments.len(), 2);
        match &result.segments[0] {
            PathSegment::Name(ident) => assert_eq!(ident.name.as_str(), "Int"),
            _ => panic!("expected Name segment"),
        }
        match &result.segments[1] {
            PathSegment::Name(ident) => assert_eq!(ident.name.as_str(), "Elem"),
            _ => panic!("expected Name segment"),
        }
    }
}
