#[cfg(test)]
mod tests {
    use super::*;

    fn term_universe(n: u32) -> Term {
        Term::Universe(n)
    }

    #[test]
    fn empty_goal_is_unconditional() {
        let g = VerificationGoal::new(
            vec![],
            term_universe(0),
            GoalSource::Theorem {
                name: "trivial".into(),
            },
        );
        assert!(g.is_unconditional());
        assert_eq!(g.hypothesis_count(), 0);
        assert_eq!(g.to_term(), Term::Universe(0));
    }

    #[test]
    fn goal_with_one_hypothesis_wraps_in_pi() {
        let g = VerificationGoal::new(
            vec![term_universe(0)],
            term_universe(0),
            GoalSource::FnContract {
                fn_name: "f".into(),
            },
        );
        assert!(!g.is_unconditional());
        assert_eq!(
            g.to_term(),
            Term::Pi(
                Box::new(Term::Universe(0)),
                Box::new(Term::Universe(0)),
            ),
        );
    }

    #[test]
    fn goal_with_three_hypotheses_wraps_left_to_right() {
        // Hypotheses [H1, H2, H3], conclusion C.
        // to_term() should produce Pi(H1, Pi(H2, Pi(H3, C))).
        let g = VerificationGoal::new(
            vec![term_universe(0), term_universe(1), term_universe(2)],
            Term::Var(0),
            GoalSource::Theorem {
                name: "test".into(),
            },
        );
        let t = g.to_term();
        assert_eq!(
            t,
            Term::Pi(
                Box::new(Term::Universe(0)),
                Box::new(Term::Pi(
                    Box::new(Term::Universe(1)),
                    Box::new(Term::Pi(
                        Box::new(Term::Universe(2)),
                        Box::new(Term::Var(0)),
                    )),
                )),
            ),
        );
    }

    #[test]
    fn audit_metadata_includes_kind_name_count() {
        let g = VerificationGoal::new(
            vec![term_universe(0), term_universe(0)],
            term_universe(0),
            GoalSource::Theorem {
                name: "my_thm".into(),
            },
        );
        let m = g.audit_metadata();
        assert_eq!(m.get("kind").map(|s| s.as_str()), Some("theorem"));
        assert_eq!(m.get("name").map(|s| s.as_str()), Some("my_thm"));
        assert_eq!(m.get("hypothesis_count").map(|s| s.as_str()), Some("2"));
    }

    #[test]
    fn audit_metadata_for_subgoal_includes_depth() {
        let g = VerificationGoal::new(
            vec![],
            term_universe(0),
            GoalSource::TacticSubgoal {
                parent: "parent_thm".into(),
                depth: 3,
            },
        );
        let m = g.audit_metadata();
        assert_eq!(m.get("kind").map(|s| s.as_str()), Some("tactic_subgoal"));
        assert_eq!(m.get("name").map(|s| s.as_str()), Some("parent_thm"));
        assert_eq!(m.get("depth").map(|s| s.as_str()), Some("3"));
    }

    #[test]
    fn goal_source_kind_tags_are_distinct() {
        let tags: Vec<&'static str> = vec![
            GoalSource::Theorem { name: String::new() }.kind_tag(),
            GoalSource::Lemma { name: String::new() }.kind_tag(),
            GoalSource::Corollary { name: String::new() }.kind_tag(),
            GoalSource::FnContract {
                fn_name: String::new(),
            }
            .kind_tag(),
            GoalSource::TacticSubgoal {
                parent: String::new(),
                depth: 0,
            }
            .kind_tag(),
            GoalSource::Refinement {
                ty_name: String::new(),
            }
            .kind_tag(),
        ];
        let unique: std::collections::BTreeSet<&'static str> = tags.iter().copied().collect();
        assert_eq!(unique.len(), tags.len(), "kind_tag must be distinct per variant");
    }

    // ----- AST converter tests -----

    use verum_ast::decl::TheoremDecl;
    use verum_ast::expr::ExprKind;
    use verum_ast::ty::{Ident, Path, PathSegment};
    use verum_ast::Literal;
    use verum_common::List;
    use verum_common::Span;

    fn path_expr(name: &str) -> Expr {
        let span = Span::dummy();
        let mut list = List::new();
        list.push(PathSegment::Name(Ident {
            name: name.into(),
            span,
        }));
        let path = Path::new(list, span);
        Expr::new(ExprKind::Path(path), span)
    }

    fn bool_true_expr() -> Expr {
        let span = Span::dummy();
        Expr::new(ExprKind::Literal(Literal::bool(true, span)), span)
    }

    #[test]
    fn from_theorem_decl_with_true_proposition() {
        let span = Span::dummy();
        let theorem = TheoremDecl::new(
            Ident { name: "trivial".into(), span },
            bool_true_expr(),
            span,
        );
        let ctx = ElabContext::new();
        let goal = from_theorem_decl(&theorem, TheoremKind::Theorem, &ctx).unwrap();
        assert_eq!(goal.conclusion, Term::Universe(0));
        assert_eq!(goal.hypothesis_count(), 0);
        assert!(matches!(goal.source, GoalSource::Theorem { ref name } if name == "trivial"));
    }

    #[test]
    fn from_theorem_decl_lemma_kind_marks_source() {
        let span = Span::dummy();
        let theorem = TheoremDecl::new(
            Ident { name: "helper".into(), span },
            bool_true_expr(),
            span,
        );
        let ctx = ElabContext::new();
        let goal = from_theorem_decl(&theorem, TheoremKind::Lemma, &ctx).unwrap();
        assert!(matches!(goal.source, GoalSource::Lemma { ref name } if name == "helper"));
    }

    #[test]
    fn from_theorem_decl_with_path_proposition_resolves_axiom() {
        let span = Span::dummy();
        let theorem = TheoremDecl::new(
            Ident { name: "predicate_thm".into(), span },
            path_expr("my_pred"),
            span,
        );
        let mut ctx = ElabContext::new();
        ctx.register_axiom("my_pred", Term::Universe(0));
        let goal = from_theorem_decl(&theorem, TheoremKind::Theorem, &ctx).unwrap();
        assert_eq!(goal.conclusion, Term::Var(0));
    }

    #[test]
    fn from_refinement_produces_predicate_obligation() {
        let mut ctx = ElabContext::new();
        ctx.register_axiom("is_positive", Term::Universe(0));
        let pred = path_expr("is_positive");
        let goal = from_refinement(Term::Universe(0), &pred, "PosInt", &ctx).unwrap();
        assert_eq!(goal.hypothesis_count(), 1, "base type is hypothesis");
        assert_eq!(goal.conclusion, Term::Var(0));
        assert!(matches!(goal.source, GoalSource::Refinement { ref ty_name } if ty_name == "PosInt"));
    }

    #[test]
    fn translate_clauses_handles_empty_list() {
        let ctx = ElabContext::new();
        let clauses: Vec<Expr> = vec![];
        let result = translate_clauses(clauses.iter(), &ctx).unwrap();
        assert!(result.is_empty());
    }

    #[test]
    fn translate_clauses_propagates_first_error() {
        let ctx = ElabContext::new();
        // path "undeclared" is not a registered axiom.
        let clauses: Vec<Expr> = vec![bool_true_expr(), path_expr("undeclared")];
        match translate_clauses(clauses.iter(), &ctx) {
            Err(ElabError::UndeclaredApplyTarget(name)) => assert_eq!(name, "undeclared"),
            other => panic!("expected UndeclaredApplyTarget, got {:?}", other),
        }
    }

    // FunctionDecl tests deferred to integration suite — the type
    // has 30+ fields without a `new()` constructor, and the from_fn_decl
    // converter is exercised end-to-end by the corpus walker.  The
    // unit tests above pin the converter's algorithm via the
    // smaller TheoremDecl + Refinement entry points which share the
    // same translate_clauses + proposition_to_term helpers.
}
