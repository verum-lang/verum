/// Per-rule signature + proof for **all 38 kernel rules**.  Each
/// returns a Lean theorem block that mirrors the Typing constructor
/// of the same name.
///
/// The architectural idiom: every per-rule lemma's RHS is a single
/// constructor reference — `@Typing.t_var`, `@Typing.t_pi`, … —
/// because the inductive's constructor shapes are designed to be
/// the lemmas' shapes.  Real proofs across the entire surface; no
/// `sorry` for any structural- or formation-level concern.
///
/// The 8 with-IOU rules' lemmas thread the per-rule axiom as a
/// hypothesis; their `#print axioms` output enumerates the 8 IOU
/// trust extensions explicitly.  Discharges so far:
///   * K_Quot_Elim       — structural premises mirroring K_Quot_Form / K_Quot_Intro.
///   * K_Elim            — same template, with per-constructor case-typing
///                         remaining the kernel's input contract.
///   * K_Universe_Ascent — collapses onto T_univ for u32-bounded
///                         universes (no transfinite heights are
///                         representable at the operational layer).
///   * K_Refine          — structural premises (base : Universe i,
///                         predicate : Pi x base (Universe 0))
///                         capturing the Bool-valued-predicate intent.
///   * K_Refine_Omega    — same shape; the finite-universe bound (i :
///                         Nat) makes the ordinal-modal-depth-bound
///                         intent vacuous at the operational layer.
/// Parsed slices of a per-rule Lean signature string.
///
/// `rule_signature_lean` returns text of shape `theorem <name> :\n
/// <TYPE> :=\n  <WITNESS>` — one for each rule with an explicit
/// shape-faithful lemma.  The main-theorem renderer needs the
/// `<TYPE>` slice to define `Soundness : KernelRule → Prop` (so each
/// case branch's goal reduces to the rule's own type), and the
/// per-rule renderer uses the `<WITNESS>` slice as the proof body
/// for `Proved` rules.
struct LeanSigParts {
    type_body: String,
    witness: String,
}

fn parse_lean_signature(sig: String) -> Option<LeanSigParts> {
    // The signature follows `theorem <name> :\n  <TYPE> :=\n  <WITNESS>`.
    let (head, witness) = sig.split_once(":=\n")?;
    // head is `theorem <name> :\n  <TYPE>` with trailing whitespace
    // before `:=`.  The first ` :\n` separates name from type body.
    let (_lemma_decl, type_body) = head.split_once(" :\n")?;
    Some(LeanSigParts {
        type_body: type_body.trim().to_string(),
        witness: witness.trim().to_string(),
    })
}

